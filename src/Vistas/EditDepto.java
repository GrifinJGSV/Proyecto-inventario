    private void txtNombreDepartamentoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreDepartamentoKeyTyped
        // TODO add your handling code here:
          String n = txtNombreDepartamento.getText();
       
        if (!(Character.isLetter(evt.getKeyChar())) && !(evt.getKeyChar() == KeyEvent.VK_SPACE)) {//&& !(evt.getKeyChar()==KeyEvent.VK_SPACE)
            // validacion desde teclado para verificar que lo esta ingresando sean letras de lo contrario nos mostrara un mensaje que diga que solo  permite letras y  incluya espacios tambien
            getToolkit().beep();
            evt.consume();

        }
        
           if (n.startsWith(" ")) {
            txtNombreDepartamento.setText(n.trim());
            return;
        }

        if (n.length() >= 30) {// esta validacion es para dar un maximo de logitud del nombre que se puede aceptar en la caja de texto
            evt.consume();
        }
    }//GEN-LAST:event_txtNombreDepartamentoKeyTyped

