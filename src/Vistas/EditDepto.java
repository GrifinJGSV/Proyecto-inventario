    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        // TODO add your handling code here:
          String nombre = txtNombreDepartamento.getText().trim();//Eliminar espacios en blanco al inicio y al final
          String ubic = txaUbicacion.getText().trim();//Eliminar espacios en blanco al inicio y al final
        

         if(nombre.isEmpty()){
             
             JOptionPane.showMessageDialog(this, "el nombre de  departamento  está vacío");
          
            return; // Salir del método si el campo está vacío
        }
            if(!nombre.matches("[a-zA-ZáéíóúñÁÉÍÓÚÑ][a-zA-Z áéíóúñÁÉÍÓÚÑü]+")) {//si el nombre del departamento contiene caracteres diferentes se muestra el siguiente mensaje
            //mensaje de error 
            
            JOptionPane.showMessageDialog(this, "alguno de los caracteres que ingresó no es valido");
            return;//sale del método
        }
       
        

         if(ubic.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "la ubicación  está vacío");
            return; // Salir del método si el campo está vacío
        }
            if(!ubic.matches("[a-zA-ZáéíóúñÁÉÍÓÚÑ,.\\s][a-zA-Z áéíóúñÁÉÍÓÚÑü,.\\s]+")) {//si el nombre del departamento contiene caracteres diferentes se muestra el siguiente mensaje
            //mensaje de error 
             JOptionPane.showMessageDialog(this, "alguno de los caracteres que ingresó no es valido");
         
            
            return;//sale del método
        }
            
          QuerysDepartamentos QProve = new QuerysDepartamentos();

          QProve.setNombreDepartamento(nombre);
          QProve.setPrefijo(txtPrefijo.getText());
          QProve.setUbicacion(ubic);
          QProve.setId(Integer.parseInt(txtId.getText()));
         if (Departamentos.Editar(QProve)) {
         JOptionPane.showMessageDialog(this, "Se ha Modificado Exitosamente el departamento");
         Departamentos.MostrarDepartamentos("");
         dispose();

        } else {
                JOptionPane.showMessageDialog(null, "hay un error, consulte con el Administrador de Sistema", "Error de base de datos", 
                    JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

