    public void mostrarDatosCliente(){
        DefaultTableModel tcompra = new DefaultTableModel();
        tcompra.addColumn("N.Producto");
        tcompra.addColumn("NOMBRE Producto");
        tcompra.addColumn("Descripcion");
        tcompra.addColumn("TIPO COMPRA");
        tcompra.addColumn("PROVEEDOR");
        
        tablacompra.setModel(tcompra);
        
        String [] datos = new String[5];
        
        try {
            Statement leer = conect.createStatement();
            ResultSet resultado = leer.executeQuery("SELECT * FROM compras");
            
            while(resultado.next()){
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                datos[3] = resultado.getString(4);
                datos[4] = resultado.getString(5);
                tcompra.addRow(datos);
            }
            tablacompra.setModel(tcompra);
        } catch (SQLException e) {  
            JOptionPane.showMessageDialog(null, e+ "Error e la connsulta");
            
        }
        
        
        
    }

